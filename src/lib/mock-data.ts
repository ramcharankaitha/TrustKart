// Initialize shops in localStorage if not already present
export const initializeShopsInStorage = () => {
  if (typeof window !== 'undefined') {
    const storedShops = localStorage.getItem('shops');
    if (!storedShops) {
      // Start with empty shops array - shops will be added through registration
      localStorage.setItem('shops', JSON.stringify([]));
    }
  }
};

// Clear all existing shops from localStorage
export const clearAllShops = () => {
  if (typeof window !== 'undefined') {
    localStorage.setItem('shops', JSON.stringify([]));
    // Dispatch event to notify components of the change
    window.dispatchEvent(new Event('shopStatusChanged'));
  }
};

